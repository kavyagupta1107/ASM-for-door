.MODEL TINY
.DATA

DOORSTATE DB 0
MOTORC EQU __

.CODE 
.EXIT

OPENDOOR PROC NEAR 
CALL START_TIMER
IN AL,MOTORC
AND AL,11111100b
OR AL,00000001b
OUT MOTORC,AL
;TIME DELAY TO ENSURE DOOR OPENS

IN AL,MOTORC
AND AL,11111100b
OUT MOTORC,AL

MOV DOORSTATE,1
OPENDOOR ENDP


CLOSEDOOR PROC NEAR

IN AL,MOTORC
AND AL,11111100b
OR AL,00000010b
OUT MOTORC,AL
;TIME DELAY TO ENSURE DOOR CLOSES

IN AL,MOTORC
AND AL,11111100b
OUT MOTORC,AL

MOV DOORSTATE,0
CLOSEDOOR ENDP




